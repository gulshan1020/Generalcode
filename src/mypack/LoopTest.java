package mypack;

public class LoopTest {

	public static void main(String[] args) {
		int number = 1;
		for(int i=1;i<5;i++) {
			//System.out.println("\t");
			for(int j=1;j<=i;j++) {
				
				System.out.println(number + " ");
				System.out.println();
				number++;
			}
			System.out.println();	
			
			
		}

	}

}
